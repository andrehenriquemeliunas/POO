package lista2.exercicio1Lista2;

public class calculadora {

    public Integer num1;
    public Integer num2;

    public int somar(int num1, int num2){
        int resultado;
        resultado = num1 + num2;
        return resultado;
    }

    public int subtrair(int num1, int num2){
        int resultado;
        resultado = num1 - num2;
        return resultado;
    }

    public int multiplicar(int num1, int num2){
        int resultado;
        resultado = num1 * num2;
        return resultado;
    }

    public int dividir(int num1, int num2){
        int resultado;
        resultado = num1 / num2;
        return resultado;
    }

}
